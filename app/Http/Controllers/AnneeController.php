     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'numcni' => 'required|unique:Annee,numcni',
            'prenom' => 'required',
            'nom' => 'required',
            'datenais' => 'required',
            'lieunais' => 'required',
            'telephone' => 'required',
            'email' => 'required',
            'password' => 'required',
        ]);

        $annee = new Annee($request->all());
        $annee->saveOrFail();

        return redirect()->route('annee.index');

    }

